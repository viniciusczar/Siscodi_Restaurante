    public void atualizaTotal(){
        double aux = 0;
        for (int i = 0; i < tbProdutosMesa.getRowCount(); i++) {
                aux += Double.valueOf(String.valueOf(tbProdutosMesa.getValueAt(i, 4)).replace(",", "."));
            }
        lbPrecoTotal.setText(decimal.format(aux));
        objMesa.atualizaValor(lbPrecoTotal.getText());
    }
    
    public void insereTabela(){
        if (!txtQuantidade.getText().equals("") && !txtQuantidade.getText().equalsIgnoreCase("0")) {
            int qtd = Produto.buscaQuantidade(Integer.valueOf(txtCodProduto.getText()));
            JOptionPane.showMessageDialog(null,qtd+ "||" +Integer.valueOf(txtQuantidade.getText()) );
            if(!(qtd < Integer.valueOf(txtQuantidade.getText()))){
            if (linhas == null) {
                linhas = new Object[5];
            }
             
            linhas[0] = txtCodProduto.getText();
            linhas[1] = txtDescricao.getText();
            linhas[2] = txtQuantidade.getText();
            linhas[3] = Float.valueOf(txtValor.getText().replaceAll(",", "."));
            linhas[4] = Float.valueOf(txtValor.getText().replaceAll(",", ".")) * Integer.valueOf(txtQuantidade.getText());

            tabelaAux = (DefaultTableModel) tbProdutosMesa.getModel();
            tabelaAux.insertRow(tabelaAux.getRowCount(), linhas);

            atualizaTotal();
            
            limpaFrame();
            }else{
                JOptionPane.showMessageDialog(null,"Quantidade insuficiente no estoque!");
            }
        }else{
           JOptionPane.showMessageDialog(this,"Insira a Quantidade!","PubManager",JOptionPane.INFORMATION_MESSAGE);
        }
            
            
        
        
    }
    
