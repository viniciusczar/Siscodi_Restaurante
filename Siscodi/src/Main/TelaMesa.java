    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        txtCodProduto.setText("");
        txtDescricao.setText("");
        txtValor.setText("");
        PesquisaProduto2 PP = new PesquisaProduto2(null,true);
        PP.setAlwaysOnTop(true);
        PP.setVisible(true);
        int id;
        id = Produto.buscaIdAuxiliar();
        Produto.setIdProduto(id);
         if (Produto.buscaProduto(Produto)) {                 
                    txtQuantidade.setEnabled(true);
                    btInserir.setEnabled(true);
                    txtCodProduto.setText(String.valueOf(Produto.getIdProduto()));
                    txtDescricao.setText(Produto.getDescricao());
                    txtValor.setText(String.valueOf(Produto.getPreco()));
                } else {
                    txtDescricao.setText("Produto Não Localizado!");
                    txtValor.setText("");
                    txtQuantidade.setEnabled(false);
                    btInserir.setEnabled(false);
                }
         Produto.apagarIdAuxiliar();
    }//GEN-LAST:event_jButton1ActionPerformed

