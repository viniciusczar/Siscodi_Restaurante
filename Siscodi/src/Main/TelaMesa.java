    private void btExcluiItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluiItemActionPerformed
        if (tbProdutosMesa.getSelectedRow() > -1){
            if (JOptionPane.showConfirmDialog(this, "Deseja Excluir o Produto Selecionado?", "PubManager.Soft",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                tabelaAux.removeRow(tbProdutosMesa.getSelectedRow());
                atualizaTotal();
            }
        }
    }//GEN-LAST:event_btExcluiItemActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (tabelaAux != null){
            if (frmCupom == null){
                frmCupom = new FormCupom();
            }

            frmCupom.setModal(true);
            frmCupom.setItensMesa(tabelaAux);
            frmCupom.setNumMesa(lbNumMesa.getText());
            frmCupom.setTotalMesa(lbPrecoTotal.getText().replaceAll(",","."));
            frmCupom.preencheLista();

            frmCupom.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Nenhum Item Vendido!",
                    "Siscodi",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

